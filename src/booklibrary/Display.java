    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       jTextArea1.setText("");
        int k=0,m=0;
        for(int i=0;i<mdata.iib;i++)
        {
            if(mdata.IB[i].did==0)
            {
                if((k++)==0)
                    jTextArea1.append("Isuuable book in library....\n");
                jTextArea1.append(mdata.IB[i].title);
                 jTextArea1.append("\n");
            }
        }
        
        for(int i=0;i<mdata.irb;i++)
        {
            if(mdata.RB[i].did==0)
            {
                if((m++)==0 )
                    jTextArea1.append("Referance book in library.....\n");
                jTextArea1.append(mdata.RB[i].title);
                jTextArea1.append("\n");
            }
        }
        if(k==0 && m==0)
            jTextArea1.append("No book is present in library\n");
        else
            jTextArea1.append("Successfully Displayed\n");
        
    
    }//GEN-LAST:event_jButton1ActionPerformed

