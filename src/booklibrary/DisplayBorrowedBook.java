    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        jTextArea1.setText("");
        int j=1;
        
         jTextArea1.append("Borrowed Book which is returned\n");
         for(int i=0;i<mdata.nbb;i++)
            if(mdata.BB[i].isreturn==1)
                jTextArea1.append("Sr no "+(j++) +"\n" +"Name of book -> " + mdata.BB[i].title +"\nName of friend -> "+mdata.BB[i].friendname+"\nDate of borrowed -> "+mdata.BB[i].DOB +"\nDate of return -> "+mdata.BB[i].DOR +"\n");
          j=1;
          jTextArea1.append("\n");
         jTextArea1.append("Borrowed Book which is still in library\n");
       
         for(int i=0;i<mdata.nbb;i++)
            if(mdata.BB[i].isreturn==0)
                jTextArea1.setText("Sr no "+(j++) +"\n" +"Name of book -> " + mdata.BB[i].title +"\nName of friend -> "+mdata.BB[i].friendname+"\nDate of borrowed -> "+mdata.BB[i].DOB +"\n ");
         
         jTextArea1.append("\n");
         
        jTextArea1.append("Successfully Displayed \n ");
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

