    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String newbook;
       newbook= jTextField1.getText();
       jTextArea1.setText("");
       
       int k=0;
      
       
        for(int i=0;i<mdata.iib;i++)
        {
            if(mdata.IB[i].title.contains(newbook)==true  && mdata.IB[i].did!=1)
            {
                if(k==0)
                {
                    jTextArea1.append("Issuable Book matched with your search\n");
                    k++;
                }
                jTextArea1.append(mdata.IB[i].title);
                jTextArea1.append("\n");
            }
        }
        int m=0;
        for(int i=0;i<mdata.irb;i++)
        {
            if(mdata.RB[i].title.contains(newbook)==true && mdata.RB[i].did!=1)
            {
                if(m==0)
                {
                     jTextArea1.append("Referance Book matched with your search\n");
                     m++;
                }
                 jTextArea1.append(mdata.RB[i].title);
                 jTextArea1.append("\n");
            }
                
        }
        if(k==0 && m==0)
            jTextArea1.append("No book with your search found\n");
        
        
       
        
    }//GEN-LAST:event_jButton1ActionPerformed

    
