    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       
       String delobj=jTextField2.getText();
       jTextArea1.setText("");
        int i,k=0,l,c=0;
        Date dt =new Date();
        
        for(i=0;i<fl.count;i++)
        {
            if(delobj.equals(fl.FR[i].name) && fl.FR[i].status!=-1 )
            {
                for(int j=0;j<fl.FR[i].nBook;j++)
                {
                    if(fl.FR[i].fbr[j].isreturn==0)
                    {
                        if((k++)==0)
                           jTextArea1.append("Books Outstanding against"+"  "+fl.FR[i].name+" are-->\n");
                         jTextArea1.append(fl.FR[i].fbr[j].BookName+"  "+fl.FR[i].fbr[j].DOI);
                    }                    
                }
                jTextArea1.append("\n");
                
                delobj=jTextField1.getText();
                for(l=0;l<data.iib;l++)
                {
                    if(delobj.equals(data.IB[l].title) && data.IB[l].did==0 && data.IB[l].isavailabile==1)
                    {
                       //  System.out.println(delobj+" "+i+" "+FR[i].nBook+ FR[i].fbr[FR[i].nBook]);
                         fl.FR[i].fbr[fl.FR[i].nBook].BookName=delobj;
                         fl.FR[i].fbr[fl.FR[i].nBook++].DOI=dt.toString();
                         data.IB[l].bh[data.IB[l].bhi].friendname=fl.FR[i].name;
                         data.IB[l].bh[data.IB[l].bhi].DOI=fl.dt.toString();
                         data.IB[l].isavailabile=0;
                         data.IB[l].bhi++;
                         c=1;
                         break;
                    }
                    
                }
                break;
            }
        }
        
        if(c==0)
            JOptionPane.showMessageDialog(null,"This book is not present in issuable book");
        if(c==1)
            JOptionPane.showMessageDialog(null,"Successfully issued");
        
    
   
    
    
    }//GEN-LAST:event_jButton2ActionPerformed

