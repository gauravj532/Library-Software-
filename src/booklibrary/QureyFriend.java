       String delobj=jTextField1.getText();
        jTextArea1.setText("");
        int k=0,i;
        for(i=0;i<fl.count;i++)
        {
            if(fl.FR[i].name.contains(delobj) && fl.FR[i].status!=-1)
            {
                if((k++)==0)
                    jTextArea1.append("Friends matches with your search\n");
                 jTextArea1.append(fl.FR[i].name+ "\n");
            }
        }
        if(k==0)
              jTextArea1.append("None is found with your search\n");
        else
             jTextArea1.append("Successfully Displayed\n");
    
